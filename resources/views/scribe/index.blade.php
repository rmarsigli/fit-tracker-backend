        <h1 id="introduction">Introduction</h1>
<p>API for fitness activity tracking with GPS, segments, and leaderboards. Track your runs, rides, and workouts with detailed statistics and compete on segments.</p>
<aside>
    <strong>Base URL</strong>: <code>http://localhost:8000</code>
</aside>
<pre><code>Welcome to the FitTrack BR API documentation. This API allows you to build fitness tracking applications with features like:

- **Activity Tracking**: Real-time GPS tracking with pause/resume capabilities
- **Segments**: Create and compete on route segments with leaderboards
- **Statistics**: Detailed metrics including pace zones, splits, and historical data
- **Social Features**: Public activity feeds and following system

All API endpoints return JSON responses and use Laravel Sanctum for authentication.

&lt;aside&gt;As you scroll, you'll see code examples for working with the API in different programming languages in the dark area to the right (or as part of the content on mobile).
You can switch the language used with the tabs at the top right (or from the nav menu at the top left on mobile).&lt;/aside&gt;</code></pre>

        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer {YOUR_BEARER_TOKEN}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>You can retrieve your authentication token by calling the <code>POST /api/v1/login</code> or <code>POST /api/v1/register</code> endpoints. Include the token in the <b>Authorization</b> header as <code>Bearer {token}</code> for all authenticated requests.</p>

